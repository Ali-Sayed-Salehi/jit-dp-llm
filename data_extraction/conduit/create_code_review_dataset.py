#!/usr/bin/env python3
"""
Create a per-commit Mozilla code-review dataset.

This script reads:

    datasets/mozilla_code_review/per_commit_drev_transactions.jsonl

It writes one JSON object per per-commit DREV row to:

    datasets/mozilla_code_review/drev_review_data.jsonl

Each output row contains:

    commit_id, dataset_split, risk_score, drev_submission_date,
    drev_closed_merged_date, drev_author, files_changed, reviews

`files_changed` is extracted from a local Mercurial clone of Mozilla's autoland
repository at:

    data_extraction/mercurial/repos/autoland

Paths in `files_changed` are canonicalized with:

    datasets/mozilla_code_review/file_path_timeline.jsonl

Generate that file first with data_extraction/conduit/get_file_path_timelines.py.

The autoland repository is cloned if missing and updated with `hg pull -u URL`
if it already exists. Pulls are retried to tolerate intermittent Mercurial HTTP
failures. Use `--debug` to process only 10 eval rows and 10 final-test rows from
per_commit_drev_transactions.jsonl.
"""

from __future__ import annotations

import argparse
from dataclasses import dataclass
from datetime import datetime, timezone
import json
from pathlib import Path
import shutil
import subprocess
import sys
import time
from typing import Any, Iterator

REPO_ROOT = Path(__file__).resolve().parents[2]
DATASET_DIR = REPO_ROOT / "datasets" / "mozilla_code_review"

DEFAULT_ALL_COMMITS_JSONL = DATASET_DIR / "all_commits.jsonl"
DEFAULT_DREV_TRANSACTIONS_JSONL = DATASET_DIR / "per_commit_drev_transactions.jsonl"
DEFAULT_OUTPUT_JSONL = DATASET_DIR / "drev_review_data.jsonl"
DEFAULT_FILE_PATH_TIMELINE_JSONL = DATASET_DIR / "file_path_timeline.jsonl"
DEFAULT_AUTOLAND_REPO = (
    REPO_ROOT / "data_extraction" / "mercurial" / "repos" / "autoland"
)
DEFAULT_AUTOLAND_URL = "https://hg-edge.mozilla.org/integration/autoland"
DEFAULT_HG_PULL_MAX_ATTEMPTS = 5
DEFAULT_HG_PULL_RETRY_BASE_SLEEP_SECONDS = 5.0
DEBUG_DATASET_SPLITS = ("eval", "final test")


@dataclass(frozen=True)
class JsonlRecord:
    line_num: int
    record: dict[str, Any]


@dataclass(frozen=True)
class InputRow:
    line_num: int
    commit_id: str
    dataset_split: str
    risk_score: float
    transactions: list[dict[str, Any]]


@dataclass(frozen=True)
class FilePathInterval:
    start_index: int
    end_index: int
    canonical_path: str


@dataclass(frozen=True)
class FilePathCanonicalizer:
    intervals_by_path: dict[str, tuple[FilePathInterval, ...]]

    def canonicalize(self, *, commit_index: int, path: str) -> str:
        intervals = self.intervals_by_path.get(path)
        if intervals is None:
            return path

        for interval in reversed(intervals):
            if interval.start_index <= commit_index <= interval.end_index:
                return interval.canonical_path
        return path


@dataclass(frozen=True)
class ChangedFiles:
    files: list[str]
    raw_file_count: int
    canonicalization_count: int


