def iter_jsonl(path: Path) -> Iterator[JsonlRecord]:
    with path.open("r", encoding="utf-8") as input_file:
        for line_num, line in enumerate(input_file, start=1):
            line = line.strip()
            if not line:
                continue
            try:
                record = json.loads(line)
            except json.JSONDecodeError as exc:
                raise ValueError(f"Invalid JSON at {path}:{line_num}: {exc}") from exc
            if not isinstance(record, dict):
                raise ValueError(f"Expected object at {path}:{line_num}")
            yield JsonlRecord(line_num=line_num, record=record)


def parse_risk_score(value: Any, *, line_label: str) -> float:
    try:
        risk_score = float(value)
    except (TypeError, ValueError) as exc:
        raise ValueError(f"{line_label}: invalid risk_score {value!r}") from exc
    if not 0.0 <= risk_score <= 1.0:
        raise ValueError(f"{line_label}: risk_score outside [0, 1]: {risk_score}")
    return risk_score


def parse_input_row(item: JsonlRecord, *, path: Path) -> InputRow:
    record = item.record
    line_label = f"{path}:{item.line_num}"

    commit_id = record.get("commit_id")
    if not isinstance(commit_id, str) or not commit_id:
        raise ValueError(f"{line_label}: missing commit_id")

    dataset_split = record.get("dataset_split")
    if not isinstance(dataset_split, str) or not dataset_split:
        raise ValueError(f"{line_label}: missing dataset_split")

    transactions = record.get("transactions")
    if not isinstance(transactions, list):
        raise ValueError(f"{line_label}: missing transactions list")
    typed_transactions: list[dict[str, Any]] = []
    for index, transaction in enumerate(transactions):
        if not isinstance(transaction, dict):
            raise ValueError(f"{line_label}: transactions[{index}] is not an object")
        typed_transactions.append(transaction)

    if "risk_score" not in record:
        raise ValueError(f"{line_label}: missing risk_score")
    risk_score = parse_risk_score(record.get("risk_score"), line_label=line_label)

    return InputRow(
        line_num=item.line_num,
        commit_id=commit_id,
        dataset_split=dataset_split,
        risk_score=risk_score,
        transactions=typed_transactions,
    )


def load_input_rows(path: Path, *, per_split_count: int | None = None) -> list[InputRow]:
    if per_split_count is not None and per_split_count <= 0:
        return []

    rows: list[InputRow] = []
    selected_counts = (
        {split: 0 for split in DEBUG_DATASET_SPLITS}
        if per_split_count is not None
        else None
    )

    for item in iter_jsonl(path):
        row = parse_input_row(item, path=path)
        if selected_counts is not None:
            if row.dataset_split not in selected_counts:
                continue
            if selected_counts[row.dataset_split] >= per_split_count:
                continue

            selected_counts[row.dataset_split] += 1
        rows.append(row)
        if selected_counts is not None and all(
            split_count >= per_split_count for split_count in selected_counts.values()
        ):
            break

    return rows


def describe_debug_split_counts(rows: list[InputRow]) -> str:
    counts = {
        split: sum(row.dataset_split == split for row in rows)
        for split in DEBUG_DATASET_SPLITS
    }
    return ", ".join(f"{counts[split]} {split}" for split in DEBUG_DATASET_SPLITS)


def load_required_commit_indices(
    path: Path,
    required_commit_ids: set[str],
) -> dict[str, int]:
    if not required_commit_ids:
        return {}

    commit_indices: dict[str, int] = {}
    commit_index = 0
    for item in iter_jsonl(path):
        node = item.record.get("node")
        if not isinstance(node, str) or not node:
            raise ValueError(f"{path}:{item.line_num}: commit is missing a valid node")

        if node in required_commit_ids:
            if node in commit_indices:
                raise ValueError(f"{path}:{item.line_num}: duplicate commit node {node}")
            commit_indices[node] = commit_index
            if len(commit_indices) == len(required_commit_ids):
                break

        commit_index += 1

    missing_commit_ids = sorted(required_commit_ids - set(commit_indices))
    if missing_commit_ids:
        preview = ", ".join(missing_commit_ids[:5])
        raise ValueError(
            f"{path} is missing {len(missing_commit_ids)} required commit ids. "
            f"First missing ids: {preview}"
        )

    return commit_indices


def parse_timeline_int(value: Any, *, line_label: str, field_name: str) -> int:
    if type(value) is int:
        return value
    raise ValueError(f"{line_label}: invalid {field_name}: {value!r}")


def parse_optional_timeline_int(
    value: Any,
    *,
    line_label: str,
    field_name: str,
) -> int | None:
    if value is None:
        return None
    return parse_timeline_int(value, line_label=line_label, field_name=field_name)


def parse_timeline_string(value: Any, *, line_label: str, field_name: str) -> str:
    if isinstance(value, str) and value:
        return value
    raise ValueError(f"{line_label}: invalid {field_name}: {value!r}")


def add_file_path_interval(
    intervals_by_path: dict[str, list[FilePathInterval]],
    *,
    path: str,
    start_index: int,
    end_index: int,
    canonical_path: str,
) -> None:
    if start_index > end_index:
        return
    intervals_by_path.setdefault(path, []).append(
        FilePathInterval(
            start_index=start_index,
            end_index=end_index,
            canonical_path=canonical_path,
        )
    )


def load_file_path_canonicalizer(path: Path) -> FilePathCanonicalizer:
    intervals_by_path: dict[str, list[FilePathInterval]] = {}
    timeline_end_index = sys.maxsize

    for item in iter_jsonl(path):
        record = item.record
        line_label = f"{path}:{item.line_num}"
        record_type = record.get("record_type", "timeline")

        if record_type == "metadata":
            scan_end_index = record.get("scan_end_index", record.get("boundary_end_index"))
            timeline_end_index = parse_timeline_int(
                scan_end_index,
                line_label=line_label,
                field_name="scan_end_index",
            )
            continue

        if record_type != "timeline":
            raise ValueError(f"{line_label}: invalid record_type {record_type!r}")

        canonical_path = parse_timeline_string(
            record.get("canonical_path"),
            line_label=line_label,
            field_name="canonical_path",
        )
        current_path = parse_timeline_string(
            record.get("start_path"),
            line_label=line_label,
            field_name="start_path",
        )
        current_start_index = parse_timeline_int(
            record.get("start_index"),
            line_label=line_label,
            field_name="start_index",
        )
        deleted_index = parse_optional_timeline_int(
            record.get("deleted_index"),
            line_label=line_label,
            field_name="deleted_index",
        )

        events = record.get("events")
        if not isinstance(events, list) or not events:
            raise ValueError(f"{line_label}: timeline has no events")

        for event_index, event in enumerate(events):
            event_label = f"{line_label}:events[{event_index}]"
            if not isinstance(event, dict):
                raise ValueError(f"{event_label}: event is not an object")

            source_path = parse_timeline_string(
                event.get("source_path"),
                line_label=event_label,
                field_name="source_path",
            )
            if source_path != current_path:
                raise ValueError(
                    f"{event_label}: source_path {source_path!r} does not match "
                    f"current path {current_path!r}"
                )

            destination_path = parse_timeline_string(
                event.get("destination_path"),
                line_label=event_label,
                field_name="destination_path",
            )
            event_commit_index = parse_timeline_int(
                event.get("commit_index"),
                line_label=event_label,
                field_name="commit_index",
            )
            add_file_path_interval(
                intervals_by_path,
                path=current_path,
                start_index=current_start_index,
                end_index=event_commit_index,
                canonical_path=canonical_path,
            )
            current_path = destination_path
            current_start_index = event_commit_index

        final_end_index = deleted_index if deleted_index is not None else timeline_end_index
        add_file_path_interval(
            intervals_by_path,
            path=current_path,
            start_index=current_start_index,
            end_index=final_end_index,
            canonical_path=canonical_path,
        )

    return FilePathCanonicalizer(
        intervals_by_path={
            path: tuple(sorted(intervals, key=lambda item: item.start_index))
            for path, intervals in intervals_by_path.items()
        }
    )


def run_hg_pull_with_retries(
    *,
    repo_path: Path,
    repo_url: str,
    max_attempts: int,
    retry_base_sleep_seconds: float,
) -> None:
    if max_attempts < 1:
        raise ValueError("max_attempts must be at least 1")
    if retry_base_sleep_seconds < 0:
        raise ValueError("retry_base_sleep_seconds must be non-negative")

    command = ["hg", "pull", "-u", repo_url]
    last_error: subprocess.CalledProcessError | None = None

    for attempt in range(1, max_attempts + 1):
        try:
            subprocess.run(command, cwd=repo_path, check=True)
            return
        except subprocess.CalledProcessError as exc:
            last_error = exc
            if attempt == max_attempts:
                break

            wait_seconds = retry_base_sleep_seconds * attempt
            print(
                f"`{' '.join(command)}` failed with exit code {exc.returncode} "
                f"(attempt {attempt}/{max_attempts}); retrying in "
                f"{wait_seconds:g}s.",
                file=sys.stderr,
            )
            if wait_seconds > 0:
                time.sleep(wait_seconds)

    raise RuntimeError(
        f"`{' '.join(command)}` failed in {repo_path} after {max_attempts} attempts."
    ) from last_error


def ensure_autoland_repo(
    *,
    repo_path: Path,
    repo_url: str,
    skip_update: bool,
    hg_pull_max_attempts: int,
    hg_pull_retry_base_sleep_seconds: float,
) -> None:
    if shutil.which("hg") is None:
        raise RuntimeError("Mercurial CLI `hg` was not found on PATH")

    hg_dir = repo_path / ".hg"
    if skip_update:
        if not hg_dir.is_dir():
            raise FileNotFoundError(
                f"--skip-repo-update was used, but no Mercurial repo exists at {repo_path}"
            )
        return

    if hg_dir.is_dir():
        print(f"Updating autoland repository at {repo_path}", file=sys.stderr)
        run_hg_pull_with_retries(
            repo_path=repo_path,
            repo_url=repo_url,
            max_attempts=hg_pull_max_attempts,
            retry_base_sleep_seconds=hg_pull_retry_base_sleep_seconds,
        )
        return

    if repo_path.exists():
        try:
            next(repo_path.iterdir())
        except StopIteration:
            repo_path.rmdir()
        else:
            raise RuntimeError(
                f"{repo_path} exists but is not a Mercurial repository. Move it "
                "aside or pass a different --autoland-repo path."
            )

    repo_path.parent.mkdir(parents=True, exist_ok=True)
    print(f"Cloning autoland repository into {repo_path}", file=sys.stderr)
    subprocess.run(["hg", "clone", repo_url, str(repo_path)], check=True)


def run_hg_capture(repo_path: Path, args: list[str]) -> str:
    result = subprocess.run(
        ["hg", *args],
        cwd=repo_path,
        check=False,
        text=True,
        stdout=subprocess.PIPE,
        stderr=subprocess.PIPE,
    )
    if result.returncode != 0:
        command = " ".join(["hg", *args])
        raise RuntimeError(
            f"`{command}` failed in {repo_path} with exit code {result.returncode}: "
            f"{result.stderr.strip()}"
        )
    return result.stdout


def get_changed_files(repo_path: Path, commit_id: str) -> list[str]:
    output = run_hg_capture(repo_path, ["status", "--change", commit_id])
    files: list[str] = []
    for line in output.splitlines():
        if not line:
            continue
        if len(line) >= 3 and line[1] == " ":
            files.append(line[2:])
        else:
            files.append(line)
    return files


def dedupe_preserving_order(items: list[str]) -> list[str]:
    seen: set[str] = set()
    deduped: list[str] = []
    for item in items:
        if item in seen:
            continue
        seen.add(item)
        deduped.append(item)
    return deduped


def get_canonical_changed_files(
    *,
    repo_path: Path,
    commit_id: str,
    commit_index: int,
    file_path_canonicalizer: FilePathCanonicalizer,
) -> ChangedFiles:
    changed_files = get_changed_files(repo_path, commit_id)
    canonical_files: list[str] = []
    canonicalization_count = 0
    for file_path in changed_files:
        canonical_file_path = file_path_canonicalizer.canonicalize(
            commit_index=commit_index,
            path=file_path,
        )
        if canonical_file_path != file_path:
            canonicalization_count += 1
        canonical_files.append(canonical_file_path)

    return ChangedFiles(
        files=dedupe_preserving_order(canonical_files),
        raw_file_count=len(changed_files),
        canonicalization_count=canonicalization_count,
    )


def unix_timestamp_to_iso(value: Any) -> str | None:
    if value is None:
        return None
    try:
        timestamp = int(value)
    except (TypeError, ValueError):
        return None
    if timestamp <= 0:
        return None
    return datetime.fromtimestamp(timestamp, tz=timezone.utc).isoformat().replace(
        "+00:00",
        "Z",
    )


def transaction_sort_key(transaction: dict[str, Any]) -> tuple[int, int]:
    date_created = transaction.get("dateCreated")
    transaction_id = transaction.get("id")
    try:
        timestamp = int(date_created)
    except (TypeError, ValueError):
        timestamp = sys.maxsize
    try:
        numeric_id = int(transaction_id)
    except (TypeError, ValueError):
        numeric_id = sys.maxsize
    return timestamp, numeric_id


def find_create_transaction(transactions: list[dict[str, Any]]) -> dict[str, Any] | None:
    create_transactions = [
        transaction
        for transaction in transactions
        if transaction.get("type") == "create"
    ]
    if create_transactions:
        return min(create_transactions, key=transaction_sort_key)
    if transactions:
        return min(transactions, key=transaction_sort_key)
    return None


def find_latest_close_transaction(
    transactions: list[dict[str, Any]],
) -> dict[str, Any] | None:
    close_transactions = [
        transaction
        for transaction in transactions
        if transaction.get("type") == "close"
    ]
    if not close_transactions:
        return None

    dated_close_transactions = [
        transaction
        for transaction in close_transactions
        if unix_timestamp_to_iso(transaction.get("dateCreated")) is not None
    ]
    return max(dated_close_transactions or close_transactions, key=transaction_sort_key)


def is_app_author(author: str | None) -> bool:
    return isinstance(author, str) and author.startswith("PHID-APPS-")


def extract_reviews(
    transactions: list[dict[str, Any]],
    *,
    include_app_comments: bool,
) -> list[dict[str, Any]]:
    review_items: list[tuple[int, int, dict[str, Any]]] = []

    for transaction in transactions:
        comments = transaction.get("comments")
        if not isinstance(comments, list):
            continue

        for comment in comments:
            if not isinstance(comment, dict):
                continue
            if comment.get("removed") is True:
                continue

            content = comment.get("content")
            raw_comment = content.get("raw") if isinstance(content, dict) else None
            if not isinstance(raw_comment, str) or not raw_comment.strip():
                continue

            author = comment.get("authorPHID") or transaction.get("authorPHID")
            if not isinstance(author, str) or not author:
                author = None
            if not include_app_comments and is_app_author(author):
                continue

            submission_date_value = comment.get("dateCreated", transaction.get("dateCreated"))
            submission_date = unix_timestamp_to_iso(submission_date_value)
            try:
                sort_timestamp = int(submission_date_value)
            except (TypeError, ValueError):
                sort_timestamp = sys.maxsize
            try:
                comment_id = int(comment.get("id"))
            except (TypeError, ValueError):
                comment_id = sys.maxsize

            review_items.append(
                (
                    sort_timestamp,
                    comment_id,
                    {
                        "author": author,
                        "submission_date": submission_date,
                        "comment": raw_comment,
                    },
                )
            )

    review_items.sort(key=lambda item: (item[0], item[1]))
    return [review for _, _, review in review_items]


def build_output_row(
    *,
    input_row: InputRow,
    changed_files_by_commit: dict[str, ChangedFiles],
    commit_indices_by_id: dict[str, int],
    autoland_repo: Path,
    file_path_canonicalizer: FilePathCanonicalizer,
    include_app_comments: bool,
    stats: dict[str, int],
) -> dict[str, Any]:
    commit_id = input_row.commit_id
    if commit_id not in changed_files_by_commit:
        changed_files = get_canonical_changed_files(
            repo_path=autoland_repo,
            commit_id=commit_id,
            commit_index=commit_indices_by_id[commit_id],
            file_path_canonicalizer=file_path_canonicalizer,
        )
        changed_files_by_commit[commit_id] = changed_files
        if changed_files.canonicalization_count > 0:
            stats["commits_with_canonicalized_paths"] += 1
    changed_files = changed_files_by_commit[commit_id]
    stats["changed_file_paths_seen"] += changed_files.raw_file_count
    stats["file_path_canonicalizations"] += changed_files.canonicalization_count

    create_transaction = find_create_transaction(input_row.transactions)
    drev_author = None
    drev_submission_date = None
    if create_transaction is not None:
        author = create_transaction.get("authorPHID")
        drev_author = author if isinstance(author, str) and author else None
        drev_submission_date = unix_timestamp_to_iso(create_transaction.get("dateCreated"))
    close_transaction = find_latest_close_transaction(input_row.transactions)
    drev_closed_merged_date = None
    if close_transaction is not None:
        drev_closed_merged_date = unix_timestamp_to_iso(
            close_transaction.get("dateCreated")
        )

    return {
        "commit_id": commit_id,
        "dataset_split": input_row.dataset_split,
        "risk_score": input_row.risk_score,
        "drev_submission_date": drev_submission_date,
        "drev_closed_merged_date": drev_closed_merged_date,
        "drev_author": drev_author,
        "files_changed": changed_files.files,
        "reviews": extract_reviews(
            input_row.transactions,
            include_app_comments=include_app_comments,
        ),
    }


def write_dataset(
    *,
    rows: list[InputRow],
    commit_indices_by_id: dict[str, int],
    autoland_repo: Path,
    file_path_canonicalizer: FilePathCanonicalizer,
    output_jsonl: Path,
    include_app_comments: bool,
) -> dict[str, int]:
    stats = {
        "rows_seen": len(rows),
        "rows_written": 0,
        "reviews_written": 0,
        "changed_file_paths_seen": 0,
        "file_path_canonicalizations": 0,
        "commits_with_canonicalized_paths": 0,
    }
    changed_files_by_commit: dict[str, ChangedFiles] = {}

    output_jsonl.parent.mkdir(parents=True, exist_ok=True)
    with output_jsonl.open("w", encoding="utf-8") as output_file:
        for input_row in rows:
            output_row = build_output_row(
                input_row=input_row,
                changed_files_by_commit=changed_files_by_commit,
                commit_indices_by_id=commit_indices_by_id,
                autoland_repo=autoland_repo,
                file_path_canonicalizer=file_path_canonicalizer,
                include_app_comments=include_app_comments,
                stats=stats,
            )

            output_file.write(json.dumps(output_row) + "\n")
            stats["rows_written"] += 1
            stats["reviews_written"] += len(output_row["reviews"])

    return stats


def main(argv: list[str] | None = None) -> None:
    args = parse_args(argv)

    all_commits_jsonl = Path(args.all_commits_jsonl)
    drev_transactions_jsonl = Path(args.drev_transactions_jsonl)
    output_jsonl = Path(args.output_jsonl)
    file_path_timeline_jsonl = Path(args.file_path_timeline_jsonl)
    autoland_repo = Path(args.autoland_repo)

    if not all_commits_jsonl.exists():
        raise FileNotFoundError(f"all_commits JSONL not found: {all_commits_jsonl}")
    if not drev_transactions_jsonl.exists():
        raise FileNotFoundError(f"DREV transaction JSONL not found: {drev_transactions_jsonl}")
    if not file_path_timeline_jsonl.exists():
        raise FileNotFoundError(
            f"File path timeline JSONL not found: {file_path_timeline_jsonl}. "
            "Run data_extraction/conduit/get_file_path_timelines.py first."
        )

    rows = load_input_rows(
        drev_transactions_jsonl,
        per_split_count=args.debug_count if args.debug else None,
    )
    if args.debug:
        print(
            f"DEBUG: selected {len(rows)} DREV transaction rows "
            f"({describe_debug_split_counts(rows)}).",
            file=sys.stderr,
        )

    commit_indices_by_id = load_required_commit_indices(
        all_commits_jsonl,
        {row.commit_id for row in rows},
    )
    file_path_canonicalizer = load_file_path_canonicalizer(file_path_timeline_jsonl)

    ensure_autoland_repo(
        repo_path=autoland_repo,
        repo_url=args.autoland_url,
        skip_update=args.skip_repo_update,
        hg_pull_max_attempts=args.hg_pull_max_attempts,
        hg_pull_retry_base_sleep_seconds=args.hg_pull_retry_base_sleep,
    )

    stats = write_dataset(
        rows=rows,
        commit_indices_by_id=commit_indices_by_id,
        autoland_repo=autoland_repo,
        file_path_canonicalizer=file_path_canonicalizer,
        output_jsonl=output_jsonl,
        include_app_comments=args.include_app_comments,
    )
    print(json.dumps(stats, indent=2), file=sys.stderr)


if __name__ == "__main__":
    main()
